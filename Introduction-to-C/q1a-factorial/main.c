#include "cs136.h"

// factorial(n) returns the factorial of n.
int factorial(int n) {
  if (n == 0) {
   return 1;
  } else {
    return n * factorial(n-1);
  }
  
}

int main(void) {
  // public test
  assert(factorial(4) == 24);
  assert(factorial(5) == 120);
  assert(factorial(2) == 2);
  assert(factorial(3) == 6);

  return EXIT_SUCCESS;
}
